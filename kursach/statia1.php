  <h2>А что же насчет интерактивных игрушек?</h2>
  <h3>Интерактивные игрушки древнего мира</h3>
  Конечно же, речь не идет о привычных нам автоматических игрушках. В те далекие времена со столь совершенными технологиями люди еще не были знакомы, но первые попытки вдохнуть в игрушечные фигурки жизнь производились уже тогда.
  <br>
  Так, при раскопках в Древнем Египте была найдена игрушечная фигурка раба у зернотерки. «Двигателем» в данном случае служила веревочка, которая при натяжении заставляла фигурку наклоняться на зернотеркой. Но это, пожалуй, самый простой и примитивный вид интерактивной игрушки.
  Куда дальше зашли древние греки. Их можно с полным правом назвать родоначальниками не только классического, но и кукольного театра. Именно для этой цели – создания уникального и удивительного зрелища создал свой автоматический театр знаменитый ученый и механик Герон. Это было достаточно сложное многокомпонентное сооружение.
  Для приведения его в движение использовалась система, включающая в себя рычаги, блоки и различного размера и колесики, а также возможности воздуха. Такой «игрушечный» театр был способен разыгрывать не только простенькие сценки, но и полноценные многоактные постановки. О том, насколько совершенными были эти «игрушки» можно судить по сохранившемуся труду этого ученого под названием «Пневматика». В этой книге описано множество вариантов создания такого рода театральных игрушек.
  <h3>Механические чудеса эпохи Просвещения</h3>
  Следующий всплеск развития инженерно-технической мысли произошел в шестнадцатом-восемнадцатом веках. Поэтому вполне закономерным является появление новых интересных игрушечных механизмов именно в этот исторический период. И, конечно же, пальму первенства в этом деле удерживают швейцарские часовщики, которые, как оказывается, создавали не только совершенные часовые механизмы.
  Одной из самых известных «двигающихся игрушек» является автоматон, состоящий из игрушечного трио каллиграфа, живописца и музыканта. Этот удивительный прибор создали отец и сын Пьер-Жак и Анри Дро. В настоящее время хранится он  в музее швейцарского города Невшателя.
  Еще более удивительными изобретениями может похвастаться француз Жак Вокансон. Он много экспериментировал и с размерами своих «роботов», и с техническим их совершенством. Но наибольшую известность ему принесли две работы: механическая утка, которая была способна не только совершать типичные для этого вида птиц действия, но даже издавать характерные звуки, пить воду и даже переваривать еду.
  Последнее стало возможным за счет того, что ученый поместил внутрь игрушки своеобразную лабораторию. Все эти чудесные изобретения способны поразить наше воображение и сегодня, когда, казалось бы, удивить нас чем-нибудь уже практически невозможно.
  Россия также может похвастаться своими умельцами. Среди них особенно выделяется И. П. Кулибин. Он является создателем едва ли не первых в мире многофункциональных карманных часов. Кроме, собственно, часов, аппарат включал в себя музыкальную «шкатулку», проигрывающую несколько мелодий, а также механизм боя часов и, о, чудо, маленький кукольный театр, персонажи которого виртуозно двигались, исполняя свои роли.
  Разумеется, назвать все эти чудо-изобретения прошлого интерактивными игрушками в прямом смысле этого слова едва ли возможно, однако, безусловно, и то, что между первыми и последними есть, как минимум, одна общая черта – эти вещи воплощают извечную мечту человека об оживлении неживых в принципе предметов.
  Оттого не только дети, но и мы, взрослые, с особым трепетом и умилением наблюдаем за движениями игрушечных фигурок людей и животных, которые так напоминают (пусть и в утрированной форме) нас с вами. Так что интерактивная игрушка на самом деле не просто игрушка, но и еще одна возможность взглянуть на себя со стороны.

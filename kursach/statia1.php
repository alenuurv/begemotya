  <h2>Появление мягких игрушек</h2>
  История происхождения плюшевых мишек, собачек и зайчиков началась в 16-17 веке. Игрушки шили из ткани и набивали соломой. Их использовали как манекены и изготавливали только для аристократов.
  В 19 веке советы по изготовлению детских игрушек печатали в модных журналах. Первое производство наладила немка Маргарет Штайфф. Женщина пошила игрушечных зверей для племянников. Ее работа понравилась соседям и вскоре Маргарет принимала постоянные заказы.
  К делу подключились сестры и отец. Штайффы открыли мастерскую, а затем в 1902 году появился знаменитый мишка Тедди, названный в честь президента США Теодора Рузвельта.

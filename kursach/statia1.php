  <h2>История возникновения игрушек для детей</h2>
   Дети играли с фигурками людей и животных за три тысячи лет до нашей эры, поэтому, когда появилась самая первая игрушка, историкам трудно проследить. С популярными в 20-21 веке йо-йо выполняли трюки еще древние греки.
  <br>
   В истории создания представителей современного игрушечного мира проще узнать, кто автор. Неваляшку оснастил «несгибаемым» механизмом технолог Нахабинской фабрики Иван Ефимкин, а «взрослую» куклу Барби изобрела американская бизнес-леди Рут Хэндлер.
   <br>


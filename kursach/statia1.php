   <h2>Первые игрушки древности</h2>
  Фигурки древних кукол вырезали из костей мамонта и мягкого известняка, изготавливали из перевязанных пучков соломы. Древнегреческие дети играли восковыми и глиняными игрушками. Лошадок ставили на колеса уже в 900 году до нашей эры.
  У древнеегипетских кукол двигалась нижняя челюсть. На территории Индии археологи находили обезьянок, свистульки в форме птичек. В азиатских странах человеческие фигурки использовали в религиозных ритуалах.
  Древние славяне делали кукольные обереги — мотанки, куватки. Они не только участвовали в играх девочек, но и защищали от болезней, берегли сон от злых духов. Мальчики играли с деревянными саблями и лошадками-качалками. В забавах дети готовились к взрослой жизни женщины-хозяйки и мужчины-воина.
  Самая древняя игрушка — глиняный мяч. Его следы сохранились в пещере троглодитов вблизи французской коммуны Дуэ-ла-Фонтэн.
